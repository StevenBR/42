#include "libft.h"

char	*ft_strncpy(char *str1, char *str2, size_t n)
{
	char *t_str1;

	t_str1 = str1;
	while (n > 0 && *str2 != '\0')
	{
		*(t_str1++) = *(str2++);
		--n;
	}
	while (n-- > 0)
		*(t_str1++) = '\0';
	return (str1);
}
